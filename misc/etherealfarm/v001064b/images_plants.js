function blueprintifyImage(im) {
  var w = im[1];
  var h = im[2];
  im = im[0];
  var res = [];
  for(var y = 0; y < h; y++) {
    res[y] = [];
    for(var x = 0; x < w; x++) {
      var c = im[y][x];
      var r = c[0];
      var g = c[1];
      var b = c[2];
      var a = c[3];
      var l = (r + g + b) / 3;
      r = l;
      g = l;
      b = 255;
      a = a * 0.5;
      res[y][x] = [Math.floor(r), Math.floor(g), Math.floor(b), Math.floor(a)];
    }
  }
  return [res, w, h];
}

